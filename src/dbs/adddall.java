    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        try{
            String dname= (String)name.getSelectedItem();
            int quantity = Integer.parseInt(dallquantity.getText());
            Connection con = ConnectionProvider.getConnection();
            String query1 ="select quantity from dall_details where d_name=?";
            PreparedStatement pst1 = con.prepareStatement(query1);
            pst1.setString(1, dname);
            ResultSet rs1 = pst1.executeQuery();
            boolean found = false; 
        
            if (rs1.next()) {
                int existingquantity=rs1.getInt("quantity");
                int newquantity=existingquantity+quantity;
                String query2="update dall_details set quantity=? where d_name=?";
                PreparedStatement pst2=con.prepareStatement(query2);
                pst2.setInt(1,newquantity);
                pst2.setString(2,dname);
                pst2.executeUpdate();
                found=true;
                JOptionPane.showMessageDialog(null, "Successfully Added To Dhall Details Table");
            }
            if (!found) {
                String query;
                query = "INSERT INTO dall_details(d_name,quantity) VALUES (?,?)";
                PreparedStatement pst = con.prepareStatement(query);
                pst.setString(1, dname);
                pst.setInt(2, quantity);
                pst.executeUpdate();
                JOptionPane.showMessageDialog(null, "Successfully Added To Dhall Details Table");
                
                
            }
        }
        catch(NumberFormatException | SQLException e)
        {
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());

        }
        
    }//GEN-LAST:event_jButton4ActionPerformed

