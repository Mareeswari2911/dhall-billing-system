    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String dall_industry = industryname.getText();
        String dname= (String)name.getSelectedItem();
        BigInteger hsn = new BigInteger(hsncode.getText());
        int bags = Integer.parseInt(bagnum.getText());
        int packing = Integer.parseInt(packnum.getText());
        int quantity = Integer.parseInt(dallquantity.getText());
        BigDecimal rate = new BigDecimal(dallrate.getText());
        BigDecimal amount = rate.multiply(new BigDecimal(quantity));

        
        
        
        try {
            Connection con = ConnectionProvider.getConnection();
            if (con == null) {
                JOptionPane.showMessageDialog(null, "Failed to establish a database connection.");
                 return;
            }
            String query;
            query = "INSERT INTO dall_supplier(dall_industry,dall_name,hsn,bags,packing,quantity,rate,amount) VALUES (?,?,?,?,?,?,?,?)";
            PreparedStatement pst = con.prepareStatement(query);
            pst.setString(1, dall_industry);
            pst.setString(2, dname);
            pst.setBigDecimal(3, new BigDecimal(hsn));
            pst.setInt(4, bags);
            pst.setInt(5, packing);
            pst.setInt(6, quantity);
            pst.setBigDecimal(7, rate);   
            pst.setBigDecimal(8, amount);

            //pst.setString(9, quality1);
            
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Successfully Added");
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

