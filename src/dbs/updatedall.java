    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
        
        Connection con = ConnectionProvider.getConnection();
        String query="select * from dall_supplier where  dall_industry =?";
        PreparedStatement pst =con.prepareStatement(query);
        pst.setString(1, updatename.getText());
        ResultSet rs=pst.executeQuery();

        if (rs.next()) {
            industryname.setText(rs.getString(1));
            dallname.setText(rs.getString(2));
            hsncode.setText(rs.getString(3));
            bagnum.setText(rs.getString(4));
            packnum.setText(rs.getString(5));
            quantity.setText(rs.getString(6));
            rate.setText(rs.getString(7));
            updatename.setEditable(false);
        }
            
        }
        catch(HeadlessException e)
        {
            
        } catch (SQLException ex) {
            Logger.getLogger(updatedealer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

