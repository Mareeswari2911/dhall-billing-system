    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String name;
        name = updatename.getText();
        String dall_industry = industryname.getText();
        String d_name= dallname.getText();
        BigInteger hsn = new BigInteger(hsncode.getText());
        int bag = Integer.parseInt(bagnum.getText());
        int pack = Integer.parseInt(packnum.getText());
        BigDecimal qty = new BigDecimal(quantity.getText().trim());
        BigDecimal ratevalue = new BigDecimal(rate.getText());
        BigDecimal total_amount = ratevalue.multiply(qty);
        try
        {
            Connection con = ConnectionProvider.getConnection();
            Statement st= con.createStatement();
            st.executeUpdate("update dall_supplier set dall_industry='" + dall_industry + "',"
            + "dall_name='" + d_name + "',"
            + "hsn='" + hsn + "',"
            + "bags='" + bag + "',"
            + "packing='" + pack + "',"
            + "quantity='" + qty + "',"
            + "rate='" + ratevalue + "' ,"
            + "amount='" + total_amount + "' "
            + "where dall_industry='" + name + "'");

            JOptionPane.showMessageDialog(null,"sucessfully updated");
            setVisible(false);
            new updatedealer().setVisible(true);
            
        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

