    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        cgstfield.setText("0.00");
        sgstfield.setText("0.00");
        igstfield.setText("0.00");
        String dname= dhalltype.getText();
        BigDecimal qty = new BigDecimal(quantity.getText());
        BigDecimal ratevalue=new BigDecimal(rate.getText());
        BigDecimal hundred = new BigDecimal("100");
        BigDecimal d_amount = qty.divide(hundred).multiply(ratevalue);
        amount.setText(d_amount.toString());
        DefaultTableModel model1 = (DefaultTableModel) jTable1.getModel();
        model1.addRow(new Object[] {
        dhalltype.getText(),
        hnscode.getText(),
        quantity.getText(),
        rate.getText(),
        d_amount.toString()
    });
    try{
            Connection con = ConnectionProvider.getConnection();
            String query1 ="select quantity from dall_details where d_name=?";
            PreparedStatement pst1 = con.prepareStatement(query1);
            pst1.setString(1, dname);
            ResultSet rs1 = pst1.executeQuery();
            boolean found = false; 
        
            if (rs1.next()) {
                int existingquantity=rs1.getInt("quantity");
                int qtyint=Integer.parseInt(quantity.getText());
                int newquantity=existingquantity-qtyint;
                if(newquantity==0)
                {
                    JOptionPane.showMessageDialog(null, "Warning : quantity for"+dname+"is now zero","Stock Warning",JOptionPane.WARNING_MESSAGE);
                }
                String query2="update dall_details set quantity=? where d_name=?";
                PreparedStatement pst2=con.prepareStatement(query2);
                pst2.setInt(1,newquantity);
                pst2.setString(2,dname);
                pst2.executeUpdate();
                found=true;
            }
            BigDecimal totalamount =BigDecimal.ZERO;
            for(int i=0;i<model1.getRowCount();i++)
            {
                Object value = model1.getValueAt(i, 4);
                if(value!=null)
                {
                    try{
                        BigDecimal Amount=new BigDecimal(value.toString());
                        totalamount=totalamount.add(Amount);
                    }
                    catch(NumberFormatException e)
                    {
                    }
                }
            }
            totalfield.setText(totalamount.toString());
            BigDecimal total = parseBigDecimalSafe(totalfield.getText());
            BigDecimal cgst = parseBigDecimalSafe(cgstfield.getText());
            BigDecimal igst = parseBigDecimalSafe(igstfield.getText());
            BigDecimal sgst = parseBigDecimalSafe(sgstfield.getText());
        // Calculate grand total as sum of all
            BigDecimal grandTotal = total.add(cgst).add(igst).add(sgst);
        // Set grand total in grandTotalfield
            grandTotalfield.setText(grandTotal.toString());
    }
            
    catch(NumberFormatException | SQLException e)
    {
        JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
    }
        
    }//GEN-LAST:event_jButton1ActionPerformed
    private BigDecimal parseBigDecimalSafe(String s) {
    try {
        return new BigDecimal(s);
    } catch (Exception e) {
        return BigDecimal.ZERO;
    }
}
